// ─── Module outputs.tf ────────────────────────────────────────────

function generateModuleOutputsTf(mod: IaCModule): string {
  const lines: string[] = [];

  // Filter out subnet outputs — subnets are inlined in VNet
  const effectiveOutputs = mod.outputs.filter(out => {
    const subnetRes = mod.resources.find(r => r.resourceType === 'subnet' && out.name.startsWith(r.identifier));
    return !subnetRes;
  });

  for (const out of effectiveOutputs) {
    lines.push(`output "${out.name}" {`);
    lines.push(`  description = "${out.description}"`);

    // For AVM modules, output comes from the module
    const r = mod.resources.find(res => out.name.startsWith(res.identifier));
    if (r && hasAvmTerraformModule(r.resourceType)) {
      lines.push(`  value       = module.${r.identifier}.resource_id`);
    } else if (r) {
      const azurermType = AZURERM_FALLBACK_RESOURCES[r.resourceType] || `azurerm_${r.resourceType.toLowerCase()}`;
      lines.push(`  value       = ${azurermType}.${r.identifier}.id`);
    } else {
      lines.push('  value       = "" # TODO: Set output value');
    }
    lines.push('}');
    lines.push('');
  }

  return lines.join('\n');
}

// ─── tfvars ───────────────────────────────────────────────────────

function generateTfvars(env: string, arch: Architecture): string {
  const profile = ENV_SIZES[env] || ENV_SIZES.development;
  const lines: string[] = [];

  lines.push(`# ${capitalize(env)} environment variables`);
  lines.push(`# Generated by AI Architecture Generator`);
  lines.push('');
  lines.push(`subscription_id = "" # TODO: Set your subscription ID`);
  lines.push(`location        = "${profile.location}"`);
  lines.push(`environment     = "${profile.shortName}"`);
  lines.push(`prefix          = ""`);
  lines.push('');
  lines.push('tags = {');
  lines.push(`  environment = "${profile.shortName}"`);
  lines.push(`  project     = "${arch.title || 'architecture'}"`);
  lines.push('  managed_by  = "terraform"');
  lines.push('}');

  return lines.join('\n');
}

const ENV_SIZES: Record<string, { shortName: string; location: string }> = {
  production: { shortName: 'prod', location: 'westeurope' },
  development: { shortName: 'dev', location: 'westeurope' },
  staging: { shortName: 'staging', location: 'westeurope' },
};

